const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {

    let firstStr = '';

    for (let i = 0; i < expr.length; i += 10) {

        const letter = expr.slice(i, i + 10);
        
        if (letter === '**********') {

            firstStr += ' ';

        } else {

            const morseSym = letter.replace(/10/g, '.').replace(/11/g, '-').replace(/0/g, '');

            firstStr += MORSE_TABLE[morseSym];
        }
    }
    
    return firstStr;
}

module.exports = {
    decode
}